package com.cg.lopingStatement;

public class ElseIf {
	public static void main(String[] args) {
		String lang="java";
	if(lang=="python") 
		{
			System.out.println("lang is python");
			
		}
		else if(lang=="c++") 
		{
			System.out.println("lang is c++");
			
		}
		else if(lang=="java");
		{
		System.out.println("lang is java");
		}
		
		
		System.out.println("lang");}
}
